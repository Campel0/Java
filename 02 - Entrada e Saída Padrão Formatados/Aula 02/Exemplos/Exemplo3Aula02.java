public class Exemplo3Aula02 {
    
    public static void main(String[] args) {
        
        double pi = 3.1415;
        double raio = 10;
        double circunferencia = 2 * pi * raio;
        double area = pi * raio * raio;

        System.out.printf( "circunferencia: %.2f\n", circunferencia );
        System.out.printf( "area: %.2f", area );
    }

}
