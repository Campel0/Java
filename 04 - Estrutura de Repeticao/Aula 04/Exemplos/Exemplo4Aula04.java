package Exemplos;

public class Exemplo4Aula04 {
    public static void main(String[] args) {
        /*
         * Estrutura while
         *
         * while (enquanto)
         *O loop while executa um bloco de código enquanto uma condição for verdadeira.
         *
         */

        int i = 1;
        while (i <= 5){
            System.out.println(i);
            i++;
        }
    }
}
