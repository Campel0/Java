package Exemplos;

public class Exemplo5Aula04 {
    public static void main(String[] args) {

        /*
         * do-while (faça enquanto)
         *
         * A diferença é que do-while executa pelo menos uma vez, pois a condição é avaliada no final.
         *
         */

        int i = 1;
        do{
            System.out.println(i);
            i++;
        }while(i <= 5);

    }
}
