package Exemplos;

public class Exemplo1Aula04 {
    public static void main(String[] args) {

        //Estrutura de repetiçãoo for

        // A estrutura for é usada quando sabemos quantas vezes um bloco de código deve ser executado.
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }
}
