public class Exemplo2Aula04 {
    public static void main(String[] args) {
        
        //Operadores de Incremento e Decremento

        int x = 5;
        System.out.println(x++); // Pós-fixado: imprime 5 e depois incrementa
        System.out.println(x);   // Agora x vale 6
        
        int y = 5;
        System.out.println(++y); // Pré-fixado: incrementa antes e imprime 6

    }
}
