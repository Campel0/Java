// Cálculo e Impressão de Quadrados com Loop For

public class Exercicio3$4 {
    public static void main(String[] args) {

        for(int i = 15; i <=30 ; i++) // laço inicializado em 15 e ira percorrer até 30
        {
            System.out.printf("%d ", i*i); //cada vez que o laço for incrementado ele multiplicara por ele mesmo
        }

    }
}
